//野指针- 指针指向位置是不可知的（随机，不确定，没有明确限制)

int* test()
{
	int a = 10;
	return &a;
}

void main()
{
	//成因
	//1.指针未初始化
	int* p;//p是局部指针变量局部变量不初始化默认随机值
	*p = 20;//非法访问内存

	//2.指针越界访问
	int arr[10] = { 0 };
	int* p1 = arr;
	for (int i = 0; i <= 10; i++)//此时会执行11次
	{
		*(p + i) = i;
		//执行10次访问完数组，第十一次跳出数组
	}
	//3.指针指向内存释放
	int* p2 = test();
	//函数结束函数内存释放a生命周期结束
	*p2 = 20;
}