#include <stdio.h>
//按位与  &
//按位或  |
//按位异或  ^
void main()
{
	int a = 5;
	int b = 4;
	//&按二进制位与-对应二进制位有0便0，全1为1
	//00000000000000000000000000000101
	//00000000000000000000000000000100
	printf("%d\n",a &b);
	//| 按位或-对应二进制位有1便为1;
	printf("%d\n", a | b);
	//^按位异或-对应二进制位-相同为0相异为1
	printf("%d\n", a ^ b);
}