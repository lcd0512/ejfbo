#include <stdio.h>
void main()
{
	char a = 3;
	//00000000000000000000000000000011
	//00000011-a
	char b = 127;
	//00000000000000000000000001111111
	//01111111-b

	//整型提升是按照变量的数据类型的符号位进行提升
	//对于无符号高位统一补零
	char c = a + b;
	//10000010-c
	//打印为整形进行提升
	//11111111111111111111111110000010-补码
	//变反码11111111111111111111111110000001
	//变原码10000000000000000000000001111110
	//截取1字节--1111110   -126
	printf("%d\n", c);
}